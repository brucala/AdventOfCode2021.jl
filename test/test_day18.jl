module test_day18

using Test
using AdventOfCode2021.Day18
using AdventOfCode2021.Day18: SFNumber

nday = 18

data = parse_input(nday)

test0 = parse_input(
"""
[[[0,[4,5]],[0,0]],[[[4,5],[2,6]],[9,5]]]
[7,[[[3,7],[4,3]],[[6,3],[8,8]]]]
[[2,[[0,8],[3,4]]],[[[6,7],1],[7,[1,6]]]]
[[[[2,4],7],[6,[0,5]]],[[[6,8],[2,8]],[[2,1],[4,5]]]]
[7,[5,[[3,8],[1,4]]]]
[[2,[2,2]],[8,[8,1]]]
[2,9]
[1,[[[9,3],9],[[9,0],[0,7]]]]
[[[5,[7,4]],7],1]
[[[[4,2],2],6],[8,7]]
"""
)

test = parse_input(
"""
[[[0,[5,8]],[[1,7],[9,6]]],[[4,[1,2]],[[1,4],2]]]
[[[5,[2,8]],4],[5,[[9,9],0]]]
[6,[[[6,2],[5,6]],[[7,6],[4,7]]]]
[[[6,[0,7]],[0,9]],[4,[9,[9,0]]]]
[[[7,[6,4]],[3,[1,3]]],[[[5,5],1],9]]
[[6,[[7,3],[3,2]]],[[[3,8],[5,7]],4]]
[[[[5,4],[7,7]],8],[[8,3],8]]
[[9,3],[[9,9],[6,[4,9]]]]
[[2,[[7,7],7]],[[5,8],[[9,3],[0,2]]]]
[[[[5,2],5],[8,[3,7]]],[[5,[7,5]],[4,4]]]
"""
)

@testset "Day$nday tests" begin
    @test reduce(SFNumber([[[[[9,8],1],2],3],4])) == SFNumber([[[[0, 9], 2], 3], 4])
    @test reduce(SFNumber([7,[6,[5,[4,[3,2]]]]])) == SFNumber([7,[6,[5,[7,0]]]])
    @test reduce(SFNumber([[6,[5,[4,[3,2]]]],1])) == SFNumber([[6,[5,[7,0]]],3])
    @test reduce(SFNumber([[3,[2,[1,[7,3]]]],[6,[5,[4,[3,2]]]]])) == SFNumber([[3,[2,[8,0]]],[9,[5,[7,0]]]])
    @test SFNumber([[[[4,3],4],4],[7,[[8,4],9]]]) + SFNumber([1,1]) == SFNumber([[[[0,7],4],[[7,8],[6,0]]],[8,1]])
    @test sum(test0) == SFNumber([[[[8,7],[7,7]],[[8,6],[7,7]]],[[[0,7],[6,6]],[8,7]]])
    @test sum(test) == SFNumber([[[[6, 6], [7, 6]], [[7, 7], [7, 0]]], [[[7, 7], [7, 7]], [[7, 8], [9, 9]]]])
    @test solve1(test) == 4140
    @test solve2(test) == 3993
end

@testset "Day$nday solutions" begin
    @test solve1(data) == 3981
    @test solve2(data) == 4687
end

end  # module
